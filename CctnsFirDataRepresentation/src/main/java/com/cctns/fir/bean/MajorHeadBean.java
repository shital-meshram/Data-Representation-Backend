package com.cctns.fir.bean;

public class MajorHeadBean {
	
	private String majorHead;
	private Integer majorHeadCd;
	private Integer firRegNum;
	private Integer firCount;
	
	public String getMajorHead() {
		return majorHead;
	}
	public void setMajorHead(String majorHead) {
		this.majorHead = majorHead;
	}
	public Integer getMajorHeadCd() {
		return majorHeadCd;
	}
	public void setMajorHeadCd(Integer majorHeadCd) {
		this.majorHeadCd = majorHeadCd;
	}
	public Integer getFirRegNum() {
		return firRegNum;
	}
	public void setFirRegNum(Integer firRegNum) {
		this.firRegNum = firRegNum;
	}
	public Integer getFirCount() {
		return firCount;
	}
	public void setFirCount(Integer firCount) {
		this.firCount = firCount;
	}
	
	
	
	

}
